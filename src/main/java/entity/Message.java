package entity;

public class Message {

    private String name;
    private boolean handStatus;

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public boolean isHandStatus() {
        return handStatus;
    }

    public void setHandStatus(boolean handStatus) {
        this.handStatus = handStatus;
    }


}
